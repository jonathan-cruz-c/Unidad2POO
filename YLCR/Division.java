class Division { // Clase que representa la operación de división
    private int a; // Atributo entero llamado 'a'
    private int b; // Atributo entero llamado 'b'

    public Division() { // Constructor vacío
    } // Fin constructor

    public int getA() { // Método get para obtener el valor de 'a'
        return a; // Retorna el valor de 'a'
    } // Fin método get

    public void setA(int a) { // Método set para asignar un valor a 'a'
        this.a = a; // Asigna el valor pasado como parámetro al atributo 'a'
    } // Fin método set

    public int getB() { // Método get para obtener el valor de 'b'
        return b; // Retorna el valor de 'b'
    } // Fin método get

    public void setB(int b) { // Método set para asignar un valor a 'b'
        this.b = b; // Asigna el valor pasado como parámetro al atributo 'b'
    } // Fin método set
}
